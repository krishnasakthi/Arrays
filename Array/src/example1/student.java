package example1;

public class student 
{
	int rollno;
	
	String name;
	
	int age;
	
	String dept;
	

	public String getDept() {
		return dept;
	}

	public void setDept(String dept) {
		this.dept = dept;
	}

	public int getRollno() 
	{
		return rollno;    //100
	}

	public void setRollno(int rollno)  //100
	{
		this.rollno = rollno;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
	
	

}
